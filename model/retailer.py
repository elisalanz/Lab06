class Retailer:
    def __init__(self, code, name, type, country):
        self.code = code
        self.name = name
        self.type = type
        self.country = country

@property
def code(self):
        return self._code
@property
def name(self):
    return self._name
@property
def type(self):
    return self._type
@property
def country(self):
    return self._country